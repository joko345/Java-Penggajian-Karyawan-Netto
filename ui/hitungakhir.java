    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
 int bar = jTable1.getSelectedRow();
    if (bar != -1) {
        String a = tabmode.getValueAt(bar, 0).toString(); // Nik
        String b = tabmode.getValueAt(bar, 1).toString(); // Nama Karyawan
        String c = tabmode.getValueAt(bar, 2).toString(); // Jabatan
        String d = tabmode.getValueAt(bar, 3).toString(); // Gaji Pokok
        String e = tabmode.getValueAt(bar, 4).toString(); // Status Kerja
        String f = tabmode.getValueAt(bar, 6).toString(); // Tanggal (kolom ke-7)

        boxNik.setSelectedItem(a);
        nama.setText(b);
        boxkerja.setSelectedItem(c);
        gaji.setText(d);
        boxStatus.setSelectedItem(e);

        try {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date tanggal = dateFormat.parse(f);
            tgl.setDate(tanggal); // Mengatur nilai tanggal di komponen JDateChooser (tgl)
        } catch (ParseException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat memparsing tanggal.");
        }
    }
  
    }//GEN-LAST:event_jTable1MouseClicked

