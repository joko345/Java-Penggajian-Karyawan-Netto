    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
  try {
        String sql = "INSERT INTO hitungan (nik, namaKaryawan, jabatan, gajiKotor, insentif, pinjaman, pajak, gajiBersih, tgl) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";

        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, boxNik.getSelectedItem().toString());
        stat.setString(2, nama.getText());
        stat.setString(3, boxkerja.getSelectedItem().toString());

        String selectedValue = boxkerja.getSelectedItem().toString();
        double jumlah = 0.0;
        if (selectedValue.equals("Administrasi")) {
            jumlah = 4000000;
        } else if (selectedValue.equals("Marketing")) {
            jumlah = 3500000;
        } else if (selectedValue.equals("Direktur")) {
            jumlah = 8000000;
        } else if (selectedValue.equals("Satpam")) {
            jumlah = 2000000;
        } else if (selectedValue.equals("Staff Teknik")) {
            jumlah = 6000000;
        } else if (selectedValue.equals("Personalia")) {
            jumlah = 5500000;
        }
        
        stat.setDouble(4, jumlah);
        stat.setString(5, insentif.getText());
        stat.setString(6, pinjam.getText());

        double pajak = 0.0;
        if (jumlah == 4000000 || jumlah == 8000000 || jumlah == 6000000 ) {
            pajak = 0.06 * jumlah;
        } else if (jumlah == 2000000 || jumlah == 3000000) {
            pajak = 0.03 * jumlah;
        }
        
        stat.setDouble(7, pajak);
        
        double insentifValue = Double.parseDouble(insentif.getText());
        double pinjamanValue = Double.parseDouble(pinjam.getText());
        double gajiBersih = jumlah - pajak + insentifValue - pinjamanValue;
        
        stat.setDouble(8, gajiBersih);

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = sdf.format(tgl.getDate());
        stat.setString(9, tanggal);

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        kosong();
        nama.requestFocus();
        dataTable();
        dataHitung();
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Error: Format angka tidak valid. " + e.getMessage());
    }
    }//GEN-LAST:event_saveActionPerformed

