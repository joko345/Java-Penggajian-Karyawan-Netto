  private void dataTable() {
    Object[] Baris = {"Id Karyawan", "Nama Karyawan", "Jabatan", "Gaji Pokok", "Status Kerja", "Gaji Bersih", "Tanggal"};
    tabmode = new DefaultTableModel(null, Baris); // Menggunakan variabel instance tabmode
    jTable1.setModel(tabmode);

    String sql = "SELECT a.nik, b.namaKaryawan, b.jabatan, a.gajiPokok, a.statusKerja, a.gajiBersih, a.tgl " +
                 "FROM akhir a " +
                 "JOIN dataKaryawan b ON b.nik = a.nik " +
                 "ORDER BY a.nik ASC";

    try {
        java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {
            String idnik = hasil.getString("nik");
            String nama = hasil.getString("namaKaryawan");
            String jabat = hasil.getString("jabatan");
            String pokok = hasil.getString("gajiPokok");
            String sts = hasil.getString("statusKerja");
            String bersih = hasil.getString("gajiBersih");
            Date tanggal = hasil.getDate("tgl");
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tgl = sdf.format(tanggal);
            String[] data = {idnik, nama, jabat, pokok, sts, bersih, tgl};
            tabmode.addRow(data);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}


