    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

         try {
        String sql = "INSERT INTO akhir (nik, namaKaryawan, jabatan, gajiPokok, statusKerja, gajiBersih, tgl) VALUES (?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement stat = conn.prepareStatement(sql);
        
        stat.setString(1, boxNik.getSelectedItem().toString());
        stat.setString(2, nama.getText());
        stat.setString(3, boxkerja.getSelectedItem().toString());
        stat.setString(4, gaji.getText());
        stat.setString(5, boxStatus.getSelectedItem().toString());
        
        // Menentukan nilai gajiBersih berdasarkan statusKerja dan gajiPokok
        double gajiPokok = Double.parseDouble(gaji.getText());
        String status = boxStatus.getSelectedItem().toString();
        double bersih = 0.0;
        if (status.equals("Pensiun")) {
            bersih = gajiPokok * 0.6;
        } else if (status.equals("Aktif")) {
            bersih = gajiPokok;
        }
        stat.setDouble(6, bersih);

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = sdf.format(tgl.getDate());
        stat.setString(7, tanggal);

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        kosong(); // Metode untuk mengosongkan input setelah penyimpanan
        nama.requestFocus(); // Fokus kembali ke field nama setelah penyimpanan
        dataTable(); // Memuat kembali data setelah penyimpanan

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan: " + e.getMessage());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Error: Format angka tidak valid. " + e.getMessage());
    }
    }//GEN-LAST:event_saveActionPerformed

