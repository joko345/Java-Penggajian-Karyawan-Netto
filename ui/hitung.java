private void dataTable() {
    Object[] Baris = {"Id Karyawan", "Nama Karyawan", "Jabatan", "Gaji Pokok", "Insentif", "Pinjaman", "Pajak", "Gaji Bersih", "Tanggal"};
    tabmode = new DefaultTableModel(null, Baris);
    jTable1.setModel(tabmode);
    
    String sql = "SELECT a.nik, b.namaKaryawan, b.jabatan, c.gajiKotor, d.insentif, e.pinjaman, a.pajak, a.gajiBersih, a.tgl " +
                 "FROM hitungan a " +
                 "JOIN dataKaryawan b ON b.nik = a.nik " +
                 "JOIN gaji c ON c.nik = a.nik " +
                 "JOIN lembur d ON d.nik = a.nik " +
                 "JOIN pinjaman e ON e.nik = a.nik";
    
    try {
        java.sql.Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);
        
        while (hasil.next()) {
            String idnik = hasil.getString("nik");
            String nama = hasil.getString("namaKaryawan");
            String jabatan = hasil.getString("jabatan");
            String gajiKotor = hasil.getString("gajiKotor");
            String insentif = hasil.getString("insentif");
            String pinjaman = hasil.getString("pinjaman");
            String pajak = hasil.getString("pajak");
            String gajiBersih = hasil.getString("gajiBersih");                                
            Date tanggal = hasil.getDate("tgl");
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tgl = sdf.format(tanggal); // Format Date to String
            String[] data = {idnik, nama, jabatan, gajiKotor, insentif, pinjaman, pajak, gajiBersih, tgl};
            tabmode.addRow(data);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}
  
